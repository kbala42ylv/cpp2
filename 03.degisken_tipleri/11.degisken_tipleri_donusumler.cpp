#include <iostream>
using namespace std;

int main()
{
    int a = 5;

    char b = 'A';

    a = a + b;

    float c = a + 3.0;

    cout << "a = " << a << endl
         << "b = " << b << endl
         << "c = " << c << endl;

    double d = 3.4;

    int e = (int)d + 2;

    cout << "d = " << d << endl;
    cout << "e = " << e << endl;
}

