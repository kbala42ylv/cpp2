#include <iostream>
using namespace std;

int main()
{

    cout << "int: " << sizeof(int) << " bayt"
         << endl;

    cout << "short int : " << sizeof(short int)
         << " bayt" << endl;

    cout << "long int : " << sizeof(long int)
         << " bayt" << endl;

    cout << "signed long int : "
         << sizeof(signed long int) << " bayt" << endl;

    cout << "unsigned long int : "
         << sizeof(unsigned long int) << " bayt" << endl;


    return 0;

return 0;
}
